void main(){
  /* DO WHILE adalah perulangan yang dapat megeksekusi minimal 1 hasil jika tidak sesuai
  berbeda dengan while loop dimana while nya terlebih dahulu yang akan diekseskusi
  dan jika tidak sesuai maka tidak akan menghasilkan apa apa*/
  // var counter = 100; //akan menghasilkan nilai 100 pada counter
  var counter = 1; //akan menghasilka  nilai 1 - 10 sesuai dengan kondisi
  do{
    print('perulangan bilangan $counter');
    counter ++;
  }while( counter <= 10);

  
}